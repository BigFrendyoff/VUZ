def distance(x1, y1, x2, y2):
    """
    Вычисляет расстояние между двумя точками на плоскости.

    Аргументы:
    x1 (float): координата x первой точки
    y1 (float): координата y первой точки
    x2 (float): координата x второй точки
    y2 (float): координата y второй точки

    Возвращает:
    float: расстояние между двумя точками

    Примеры:
    >>> distance(0, 0, 3, 4)
    5.0
    >>> distance(0, 0, 0, 0)
    0.0
    >>> distance(1, 2, 1, 2)
    0.0
    >>> distance(-1, -1, 2, 2)
    4.242640687119285
    """
    return ((x2 - x1)**2 + (y2 - y1)**2) ** 0.5

